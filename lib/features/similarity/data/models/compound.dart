class Compound {
  const Compound({
    required this.name,
    required this.cid,
    required this.score,
    required this.smiles,
    required this.imageUrl,
    required this.explanation,
  });
  final String name;
  final String cid;
  final double score;
  final String smiles;
  final String imageUrl;
  final String explanation;
}
